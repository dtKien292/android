package com.example.petapp2;

public class hsba {
    String time_hsba;
    String detail_hsba;

    public hsba(String time, String detail_hdtn) {
        this.time_hsba = time;
        this.detail_hsba = detail_hdtn;
    }

    public hsba() {
    }

    public String getTime_hsba() {
        return time_hsba;
    }

    public void setTime_hsba(String time_hsba) {
        this.time_hsba = time_hsba;
    }

    public String getDetail_hsba() {
        return detail_hsba;
    }

    public void setDetail_hsba(String detail_hsba) {
        this.detail_hsba = detail_hsba;
    }
}
